    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nim = txtNIM.getText();
    String nama = txtNama.getText();
    long saldo = Long.parseLong(txtSaldo.getText());
    Mahasiswa mhs = new Mahasiswa(nim, nama, saldo);
    int cekSukses = mhsCtrl.simpanMahasiswa(mhs);
    if (cekSukses > 0){
        JOptionPane.showMessageDialog(this, "Data berhasil di-input!");
    }else {
        JOptionPane.showMessageDialog(this, "Data gagal di-input");
    }
    mhsCtrl.simpanMahasiswa(mhs);
    }//GEN-LAST:event_jButton1ActionPerformed

